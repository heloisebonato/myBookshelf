package com.example.mybookshelf;

public class Usuario {
    private String PlainTextEmail;
    private String PlainTextSenha;

    public Usuario() {}

    public String getPlainTextEmail() {
        return PlainTextEmail;
    }

    public void setPlainTextEmail(String PlainTextEmail) {
        this.PlainTextEmail = PlainTextEmail;
    }

    public String getPlainTextSenha() {
        return PlainTextSenha;
    }

    public void setPlainTextSenha(String PlainTextSenha) {
        this.PlainTextSenha = PlainTextSenha;
    }
}

